    private void btn_insertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_insertMouseClicked
        // TODO add your handling code here:
        if(tfd_idPerson.getText().length() > 0 || tfd_name.getText().length() > 0 || tfd_lastName.getText().length() > 0 || tfd_secondLastName.getText().length() > 0
        || ftf_date.getText().length() > 0 || tfd_nationality.getText().length() > 0 || tfd_IdFather.getText().length() > 0){
                Person person1 = new Person(tfd_idPerson.getText(), tfd_name.getText(), tfd_lastName.getText(),tfd_secondLastName.getText(),ftf_date.getText(),tfd_nationality.getText(),tfd_IdFather.getText());
                if(!theManager.hasFather(list, person1))
                    list.add(person1);
                //theManager.insertPerson(person1);
                tfd_IdFather.setText("");
                tfd_name.setText("");
                tfd_lastName.setText("");
                tfd_secondLastName.setText("");
                tfd_nationality.setText("");
                tfd_idPerson.setText("");
                ftf_date.setText("");
                
                showTable();
            
        }
    }//GEN-LAST:event_btn_insertMouseClicked

