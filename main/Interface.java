    private void btn_xmlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xmlActionPerformed
        for (int i = 0; i < list.size(); i++) {
            try {
                theManager.insertPerson(list.get(i));
            } catch (IOException ex) {
                System.out.println("Error ");
            }
        }
        if(list.size()>0){
            JOptionPane.showMessageDialog(null, "Se ha creado el archivo exitosamente!","XML",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_xmlActionPerformed

