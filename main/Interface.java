    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        if(tfd_idMember.getText().length()>0){
            String id = tfd_idMember.getText();
        
            for (int i = 0; i < list.size(); i++) {
                if(list.get(i).getId().equalsIgnoreCase(id)){
                    list.remove(i);
                    i = list.size();
                }       
            }
        showTable();
        }else{
            JOptionPane.showMessageDialog(null, "Id member field is empty!","Removing Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_updateMouseClicked
        // TODO add your handling code here:
        if(tbl_family.getSelectedRow()!=-1){
            int row = tbl_family.getSelectedRow();
            Person person1 = new Person(tfd_idPerson.getText(), tfd_name.getText(), tfd_lastName.getText(),tfd_secondLastName.getText(),ftf_date.getText(),tfd_nationality.getText(),tfd_IdFather.getText());
            
            list.remove(row);
            
            list.add(person1);
            
            JOptionPane.showMessageDialog(null, "Se ha actualizado el usuario id: "+tfd_idPerson.getText()+", exitosamente!","Update",JOptionPane.INFORMATION_MESSAGE);
            
            showTable();
            tfd_IdFather.setText("");
            tfd_name.setText("");
            tfd_lastName.setText("");
            tfd_secondLastName.setText("");
            tfd_nationality.setText("");
            tfd_idPerson.setText("");
            ftf_date.setText("");
        }
    }//GEN-LAST:event_btn_updateMouseClicked

    private void tbl_familyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_familyMouseClicked
        // TODO add your handling code here:
        if(tbl_family.getSelectedRow()!=-1){
            int row = tbl_family.getSelectedRow();
            tfd_idPerson.setText(tbl_family.getValueAt(row, 0).toString());
            tfd_name.setText(tbl_family.getValueAt(row, 1).toString());//columna 0=nombre
            tfd_lastName.setText(tbl_family.getValueAt(row, 2).toString());//columna 1=apellido
            tfd_secondLastName.setText(tbl_family.getValueAt(row, 3).toString());//columna 2=telf
            ftf_date.setText(tbl_family.getValueAt(row, 4).toString());
            tfd_nationality.setText(tbl_family.getValueAt(row, 5).toString());
            tfd_IdFather.setText(tbl_family.getValueAt(row, 6).toString());
        }
        
    }//GEN-LAST:event_tbl_familyMouseClicked

    public void showTable(){
        String Object[][] = new String[list.size()][8];
        
        for (int i = 0; i < list.size(); i++) {
            Object[i][0] = list.get(i).getId();
            Object[i][1] = list.get(i).getName();
            Object[i][2] = list.get(i).getLastname();
            Object[i][3] = list.get(i).getSecondLastname();
            Object[i][4] = list.get(i).getBirthDate();
            Object[i][5] = list.get(i).getNationality();
            Object[i][6] = list.get(i).getFathersId();
            Object[i][7] = list.get(i).getChild().toString()+", ";
            
        }
        
        tbl_family.setModel(new javax.swing.table.DefaultTableModel(
            Object,
            new String [] {
                "id", "Name", "Lastname", "SecondLastname", "Birthdate", "Nationality", "Father's id", "Children"
            }
        ));
    }
    
